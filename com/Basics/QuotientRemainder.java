package Basics;

public class QuotientRemainder {
    public static void main(String[] args) {
        int num1= 23;
        int num2= 24;

        int quotient= num1/num2;
        int remainder=num1%num2;
        System.out.println("Quotient = " + quotient);
        System.out.println("Remainder = " + remainder);
    }
}
